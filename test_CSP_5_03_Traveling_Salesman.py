import CSP_5_03_Traveling_Salesman as HW

def test_full_tsp():
    assert HW.full_TSP([[30,100],[500,200],[300,300],[200,400],[350,150],[700,120],[10,10]]) ==([30, 100], [500, 200], [700, 120], [350, 150], [300, 300], [200, 400], [10, 10])
    assert HW.full_TSP([[80,75],[100,20],[530,300],[280,200],[350,150],[700,120],[10,10]]) ==([80, 75], [100, 20], [530, 300], [700, 120], [350, 150], [280, 200], [10, 10])


def test_hueristic_tsp():
    assert HW.hueristic_TSP([[30,100],[500,200],[300,300],[200,400],[350,150],[700,120],[10,10]]) ==[[30, 100], [10, 10], [200, 400], [300, 300], [350, 150], [500, 200], [700, 120]]
    assert HW.hueristic_TSP([[80,75],[100,20],[530,300],[280,200],[350,150],[700,120],[10,10]]) ==[[80, 75], [100, 20], [10, 10], [280, 200], [350, 150], [530, 300], [700, 120]]
